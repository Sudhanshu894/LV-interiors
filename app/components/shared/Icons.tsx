// Get service icon by service ID
export const getServiceIcon = (serviceId: number) => {
  switch (serviceId) {
    case 1:
      return <ElectricIcon />;
    case 2:
      return <WoodIcon />;
    case 3:
      return <PaintBrushIcon />;
    case 4:
      return <HammerIcon />;
    case 5:
      return <DesignIcon />;
    default:
      return <PaintBrushIcon />;
  }
};

